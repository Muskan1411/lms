        String libno=jTextField1.getText();
        if(libno.isEmpty())
            JOptionPane.showMessageDialog(this, "Please Enter Library Card No.","Attention",3);
        else
        {
            try {
                Class.forName("java.sql.DriverManager");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/LibraryManagement","root","root");
                Statement stmt=con.createStatement();
                String str="select * From librarycard where LibcardNo="+libno+";";
                ResultSet rs=stmt.executeQuery(str);
                if(rs.next())
                {
                String str1="Delete from LibraryCard Where LibCardNo="+libno+";";
                stmt.executeUpdate(str1);
                JOptionPane.showMessageDialog(this, "Member Remove", "Message", 1);
                }
                else
                JOptionPane.showMessageDialog(this, "Member does not exist.","Error",0);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,e,"Error",0);
            }
        }
